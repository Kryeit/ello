class Product {
    constructor(id, name, description, price, size, color, material, virtual, listed) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.price = price;
        this.size = size;
        this.color = color;
        this.material = material;
        this.virtual = virtual;
        this.listed = listed;
    }
}

export default Product;